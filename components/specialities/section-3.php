            <h3>Urology Department</h3>
            <p id="department-content">Urology is a part of health care that deals with diseases of the male and female
                urinary tract (kidneys,
                ureters, bladder and urethra). It also deals with the male organs that are able to make babies (penis,
                testes, scrotum, prostate, etc.). Since health problems in these body parts can happen to everyone,
                urologic health is important.</p>
            <p id="department-content">Urology is known as a surgical specialty. Besides surgery, a urologist is a
                doctor with wisdom of
                internal medicine, pediatrics, gynecology and other parts of health care. This is because a urologist
                encounters a wide range of clinical problems.</p>
