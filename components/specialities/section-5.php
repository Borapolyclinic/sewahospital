            <p id="section-5-label">What is <span>Ayushman Bharat </span>Pradhan Mantri Jan Arogya Yojana?</p>
            <p>PM-JAY provide cover of Rs. 5 lakhs per family per year for secondary and tertiary care hospitalization
                to
                over
                10.74 crores poor and vulnerable families (approximately 50 crore beneficiaries). There is no cap on the
                family
                size under the scheme. This scheme was earlier known as National Health Protection Scheme (NHPS) before
                it
                was
                rechristened to PM-JAY. This scheme was launched on 23rd September 2018 by the Hon’ble Prime Minister
                Shri
                Narendra Modi.</p>

            <p>PM-JAY has been rolled out for the bottom 40% of poor and vulnerable population. The households included
                are
                based on the deprivation and occupational criteria of Socio-Economic Caste Census 2011 (SECC 2011) for
                rural
                and
                urban areas respectively. The scheme subsumed then existing Rashtriya Swasthya Bima Yojana (RSBY),
                launched
                in
                2008. Therefore, the coverage mentioned under PM-JAY also includes families that were covered in RSBY
                but
                were
                not present in the SECC 2011 database. PM-JAY is completely funded by the Government, and cost of
                implementation
                is shared between Central and State Governments.</p>
        </div>

        <div class="specialities-section-5-card">
            <p id="section-5-label"><span>Key Features</span> of PMJAY</p>
            <ul>
                <li>PM-JAY provides cover of Rs. 5 lakhs per family per year, for secondary and tertiary care
                    hospitalization
                    across public and private empaneled hospitals in India.</li>
                <li>Over 10.74 crore poor and vulnerable entitled families (approximately 50 crore beneficiaries) are
                    eligible
                    for these benefits.</li>
                <li>PM-JAY provides cashless access to health care services(Indoor) for the beneficiary at the point of
                    service,
                    that is, the hospital.</li>
                <li>PM-JAY will help reduce catastrophic expenditure for hospitalizations, which pushes 6 crore people
                    into
                    poverty each year, and will help mitigate the financial risk arising out of catastrophic health
                    episodes.
                </li>
                <li>No restrictions on family size, age or gender.</li>
                <li>All pre–existing conditions are covered from day one.</li>
                <li>Benefits of the scheme are portable across the country i.e. a beneficiary can visit any empanelled
                    public or
                    private hospital for cashless treatment.</li>
